    void cargar (String valor){
    
        String [] titulos = {"Nombre","Apellidos","DNI","DirecciÃ³n","Email","Tel","Cell","Fecha","Nro. Tarjeta"};
        String [] registros = new String[10];
        
        String sql = "SELECT * FROM reg_tarj where CONCAT (nom,' ',ape) LIKE '%"+valor+"%'";
        
        model = new DefaultTableModel(null,titulos);
        acceso_java cc=new acceso_java ();
        Connection cn = cc.conxion();
        
        try {
            
            
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            
            
            while(rs.next()){
               registros[0]=rs.getString("nom");
               registros[1]=rs.getString("ape");
               registros[2]=rs.getString("dni"); 
               registros[3]=rs.getString("dir"); 
               registros[4]=rs.getString("mail"); 
               registros[5]=rs.getString("tel");
               registros[6]=rs.getString("cell");
               registros[7]=rs.getString("fecha");
               registros[8]=rs.getString("nt");
               //registros[9]=rs.getString("mdc");
               
               
               model.addRow(registros);
 
            }
            t_reg.setModel(model);
            
          }catch(SQLException ex) {
              JOptionPane.showMessageDialog(null, ex);
          }
        
        } 
 

