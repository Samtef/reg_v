    private void t_regMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_t_regMouseClicked
        // TODO add your handling code here:
      int fila = t_reg.rowAtPoint(evt.getPoint());
      if (fila > -1){
                
                t_nom.setText(String.valueOf(t_reg.getValueAt(fila, 0)));
                t_ape.setText(String.valueOf(t_reg.getValueAt(fila, 1)));
                t_dni.setText(String.valueOf(t_reg.getValueAt(fila, 2)));
                t_dir.setText(String.valueOf(t_reg.getValueAt(fila,3)));
                t_mail.setText(String.valueOf(t_reg.getValueAt(fila,4)));
                t_tel.setText(String.valueOf(t_reg.getValueAt(fila, 5)));
                t_cell.setText(String.valueOf(t_reg.getValueAt(fila,6)));
                t_fecha.setText(String.valueOf(t_reg.getValueAt(fila, 7)));
                t_nt.setText(String.valueOf(t_reg.getValueAt(fila,8)));

        }  
        
    }//GEN-LAST:event_t_regMouseClicked

