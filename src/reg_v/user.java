    private void cuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cuActionPerformed
  
    if (t_fecha.getDate()==null) { 
         
          JOptionPane.showMessageDialog(null,"NO OLVIDES LA FECHA","Error",JOptionPane.ERROR_MESSAGE);
          
    }      
          
   else if (t_nom.getText().isEmpty()) { 
         
          JOptionPane.showMessageDialog(null,"Ups... tienes que Colocar Nombre","Error",JOptionPane.ERROR_MESSAGE);
    
     } 
   
   else if (t_ape.getText().isEmpty()) { 
         
          JOptionPane.showMessageDialog(null,"Ups... tienes que Colocar Apellido","Error",JOptionPane.ERROR_MESSAGE);
         
         
     }
    
    else if (t_dni.getText().isEmpty()) { 
         
          JOptionPane.showMessageDialog(null,"Ups... tienes que Colocar DNI","Error",JOptionPane.ERROR_MESSAGE);
         
         
    }    
        
 
    else if (t_us.getText().isEmpty()) { 
         
          JOptionPane.showMessageDialog(null,"Ups... tienes que Colocar UN USUARIO","Error",JOptionPane.ERROR_MESSAGE);
         
         
    }    
      
     else if (t_pass.getText().isEmpty()) { 
         
          JOptionPane.showMessageDialog(null,"Ups... tienes que Colocar UNA CONTRASE√ëA","Error",JOptionPane.ERROR_MESSAGE);
         
         
    }   
     
    else if (c_tie.getSelectedItem()=="Escoja una") { 
         
          JOptionPane.showMessageDialog(null,"UPS... TIENE QUE ESCOJER UNA TIENDA","Error",JOptionPane.ERROR_MESSAGE);
         
     }   
     
    else if (c_us.getSelectedItem()=="Escoja un") { 
         
          JOptionPane.showMessageDialog(null,"UPS... TIENE QUE ESCOJER UNA TIPO DE USUARIO","Error",JOptionPane.ERROR_MESSAGE);  
          
          
   }else{
        
        if (JOptionPane.showConfirmDialog(null,"CONFIRMA QUE LOS DATOS ESTEN CORRECTOS","CONFIRMAR",1)==0){ 
    
        
        String d = t_dni.getText();
        consultar(d);  
        String nus = t_us.getText();
        consultar2(nus);
        SimpleDateFormat sdf = new SimpleDateFormat("dd-MM-yyyy");
        acceso_java cc = new acceso_java ();
        Connection cn = cc.conxion();
        String sql="";
        String nom,ape,dni,us,pass,fecha,tus,tien;
        nom = t_nom.getText();
        ape = t_ape.getText();
        dni = t_dni.getText();
        us = t_us.getText();
        pass = t_pass.getText();
        tien = c_tie.getSelectedItem().toString();
        tus = c_us.getSelectedItem().toString();
        String fechad = sdf.format(t_fecha.getDate()); 
        
        
   
        sql= "INSERT INTO usuario (fecha,nom,ape,dni,nick,pass,tie,tuser) VALUES(?,?,?,?,?,?,?,?)";
        
        try{
            
          PreparedStatement pst = cn.prepareStatement(sql);
          
           pst.setString(1,fechad);
           pst.setString(2,nom);
           pst.setString(3,ape);
           pst.setString(4,dni);
           pst.setString(5,us);
           pst.setString(6,pass);
           pst.setString(7,tien);
           pst.setString(8,tus);
        
    
           int n=pst.executeUpdate();
           if (n>0){
               JOptionPane.showMessageDialog(null,"Registro Guardado con Exito");
               limpiar();
               
           }
                 
                 
        }catch (SQLException ex) {
            Logger.getLogger(n_clie.class.getName()).log(Level.SEVERE, null, ex);
            
          }
        }
      }
    }//GEN-LAST:event_cuActionPerformed

