    void consultar (String d){
       
        acceso_java cc = new acceso_java ();
        Connection cn = cc.conxion();
        String cap="";
       
        
        String sql= "SELECT * FROM usuario WHERE dni = '"+d+"'";
        
        try{
            
         Statement st = cn.createStatement();
         ResultSet rs = st.executeQuery(sql);
         
         while(rs.next ()){
             cap = rs.getString("dni");
             
          }
           if (t_dni.getText().equals(cap)){
 
               JOptionPane.showMessageDialog(null,"USUARIO YA REGISTRADO","Error",JOptionPane.ERROR_MESSAGE);
               System.exit(0);
               
             }
                
         
         }catch (SQLException ex) {
            Logger.getLogger(n_clie.class.getName()).log(Level.SEVERE, null, ex);
            
        }
          
    }
    
